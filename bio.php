                <h2 id="bio">Biography</h2>
<p>            
Steve Howard’s work with stellar artists and producers has made him a valuable
asset through an extensive and varied career. As an expert in the fields of R&B,
Blues, Rock, and Jazz, his talents have been honed to adapt to many genres and
styles of music. As a result, he is able to function not only as a performer, but also
as an arranger both in advance and on the spot with head arrangements. Steve
has been a valued arranger in session work with Albert Collins, Paul McCartney,
and others. He possesses a solid ability to give artists the horn arrangements that
they visualize for their projects, and he has frequently functioned as a working
part of production.
</p>
<p>
Steve began studying the trumpet at age 10. In the early 1970’s he attended the
world-famous jazz studies program at North Texas State University, now the
University of North Texas. During his student years he worked in the Dallas-Fort
Worth area, recording, playing shows, and touring locally with big bands including
Les Elgart and Bob Crosby when they came to the area. Original influences were
Clifford Brown, Lee Morgan, Blue Mitchell, Freddie Hubbard, and Johnny Coles.
Steve acquired the nickname “Catfish” early in his career. His family owned and
operated a famous catfish restaurant opened by his grandparents in 1945. He
worked there from childhood until he moved away to begin his career, and over
the years many of Steve's student friends had part-time work at the restaurant to
help finance their educations.
</p>
<p>
In 1973 Steve moved to New Orleans to work with blues Legend Luther Kent.
While working with Luther, he was discovered by legendary producer Allen
Toussaint. He performed on Toussaint-produced albums Nightbirds by Patti
LaBelle, which featured the #1 hit “Lady Marmalade”, and Toussaint's own
critically acclaimed Southern Nights album. At Sea-Saint Studios Steve performed
with the Meters, Dr. John, Little Feet, Tracy Nelson, Ernie K-Doe, and Lee Dorsey.
Working alongside Allen Toussaint with his unique horn arrangements strongly
influenced Steve’s concepts of horn arrangements and music production. His early
years in New Orleans working with Allen and Luther helped complete Steve's
musical education, most particularly in the field of R&B.
</p>
<p>
Paul McCartney came to New Orleans in 1974 to record at Sea-Saint. Steve was
invited to play on “Call Me Back Again” which was featured on the Venus and
Mars album. As a result, six months later Steve was asked to join Wings on their
world tour. He spent the next five years performing in concerts in the USA,
Europe, and Australia and recording at Abbey Road Studios in London as well as
making five albums with Wings. He also appeared in films of the American tour
and the Rockestra sessions. Rockestra included performances with rock legends
Pete Townsend, John Bonham, Dave Gilmore, Ronnie Lane, and John Paul Jones.
During a break in the Wings schedule Steve toured with both Tom Jones and Ray
Charles. In Ray's band he played sitting next to Johnny Coles, one of his earliest
musical influences.
</p>
<p>
Back home in the Dallas area during the 1980's Steve continued playing shows
and recording albums. He rekindled his relationship with Dr. John (Mac
Rebennack) and worked with him extensively while developing a friendship that
endured until Mac's death in 2019. During this period Steve performed at Austin
City Limits with Delbert McClinton and played concerts with the Spinners, the
Four Tops, Martha Reeves, and Gladys Knight in the Dallas area.
</p>
<p>
In 1989 Steve joined Albert Collins' band, touring worldwide and recording with
Albert until his death in 1993. With Alberts band Steve played at the Mt. Fuji Jazz
Festival in Japan and the Montreux Jazz Festival in Switzerland. The band toured
in the USA, Europe, and Australia, played Austin City Limits, and recorded
numerous albums.
</p>
<p>
In 1994 Steve returned to New Orleans as a member of the Legendary White
Trash Horns. During 1994-95 the White Trash Horns worked with producer John
Snyder on albums for Junior Wells, Clarence “Gatemouth” Brown, Billy Branch,
Larry Garner, and others. They also worked with producer Mike Post on
recordings for Sonny Landreth. Steve performed on a solo album with former
Blood, Sweat, and Tears lead singer Jerry Fisher. In addition to work with the
White Trash Horns, Steve played on numerous recordings for blues label Black
Top Records. Recordings for Black Top included albums for Solomon Burke, Guitar
Shorty, Snooks Eaglin, Phillip Walker, W.C. Clark, and others. He also performed at
the New Orleans Jazz and Heritage Festival with Boz Scaggs as well as with
Clarence “Gatemouth” Brown and Luther Kent. In 1996 and again in 2000 the
</p>
<p>
White Trash Horns reunited with Edgar Winter for two albums and live
performances including the Montreux Jazz Festival.
From 1998-2002, as a member of the White Trash Horns, Steve toured and
recorded with German superstar Peter Maffay in Europe.
</p>
<p>
In 2002 Steve formed his own band, the N'awlins Gumbo Kings, which performs
its own special brand of New Orleans-influenced music. The group has released
three albums, and old friend Dr. John appeared as a guest artist on the second
album. The group was also invited to perform as a special guest band at Levon
Helm's Midnight Ramble in Woodstock, New York. The Gumbo Kings band was
born out of Steve's love for the music of the Crescent City. Both Steve and his co-
leader and partner, clarinetist Mike Sizer, had lived and worked in New Orleans at
different times in their careers. An accidental meeting between the two resulted
in an enthusiastic conversation about the possibilities of putting together a band
of top-shelf Dallas musicians to perform New Orleans-style music. Their
repertoire includes traditional jazz, second line New Orleans funk, Louis Prima
style, and their own original songs. An active and thriving band, the Kings still
perform regularly.
</p>
<p>
In 2007 Steve was tapped to play with the Original Blues Brothers Band, filling in
for Alan “Mr. Fabulous” Rubin, and joining the band full time in 2011. The band
includes original members Steve “The Colonel” Cropper, “Blue Lou” Marini,
Murphy Dunne, and Tom “Bones” Malone. Other members of the group are
Saturday Night Live musical director Leon Pendarvis and legendary New York
session guitarist John Tropea. Steve recorded a 2017 album with the group, The
Last Shade of Blue Before Black. The OBBB has continued to travel the world and
perform concerts to sold out audiences in Europe, Japan, Australia, and Russia.            
</p>
